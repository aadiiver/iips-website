            <h3 class="text-center"><b>Open Source Initiatives</b></h3>
            <p class="text-justify">Open-source software (OSS) is computer software with its source code made available with a license in which the copyright holder provides the rights to study, change, and distribute the software to anyone and for any purpose.</p>
            <h5>We Recommend Using</h5><br>
            
            <a href="http://www.linuxfoundation.org/what-is-linux">Linux</a><br>
            <p class="text-justify">Linux is, in simplest terms, an operating system. It is the software on a computer that enables applications and the computer operator to access the devices on the computer to perform desired functions. The operating system (OS) relays instructions from an application to, for instance, the computer's processor. The processor performs the instructed task, then sends the results back to the application via the operating system.</p>
            
            <a href="https://www.python.org/">Python</a><br>
            <p class="text-justify">Python is a widely used high-level, general-purpose, interpreted, dynamic programming language. Its design philosophy emphasizes code readability, and its syntax allows programmers to express concepts in fewer lines of code than would be possible in languages such as C++ or Java. The language provides constructs intended to enable clear programs on both a small and large scale.</p>
            
            <a href="http://php.net/">PHP</a><br>
            <p class="text-justify">PHP is a server-side scripting language designed for web development but also used as a general-purpose programming language. Originally created by Rasmus Lerdorf in 1994, the PHP reference implementation is now produced by The PHP Group. PHP originally stood for Personal Home Page, but it now stands for the recursive backronym PHP: Hypertext Preprocessor.</p>
            
            <a href="https://nodejs.org/en/">Node.js</a><br>
            <p class="text-justify">In software development, Node.js is an open-source, cross-platform runtime environment for developing server-side Web applications. Although Node.js is not a JavaScript framework, many of its basic modules are written in JavaScript, and developers can write new modules in JavaScript.</p>

            <a href="https://www.mozilla.org/en-US/?v=b">Mozilla</a><br>
            <p class="text-justify">Mozilla is a free-software community, created in 1998 by members of Netscape. The Mozilla community uses, develops, spreads and supports Mozilla products, thereby promoting exclusively free software and open standards, with only minor exceptions.</p>

            <a href="https://git-scm.com/">Git</a><br>
            <p class="text-justify">Git is a widely used source code management system for software development. It is a distributed revision control system with an emphasis on speed, data integrity, and support for distributed, non-linear workflows. Git was initially designed and developed in 2005 by Linux kernel developers (including Linus Torvalds) for Linux kernel development.</p>

            <a href="https://github.com/">Github</a><br>
            <p class="text-justify">GitHub is a web-based Git repository hosting service. It offers all of the distributed revision control and source code management (SCM) functionality of Git as well as adding its own features. Unlike Git, which is strictly a command-line tool, GitHub provides a Web-based graphical interface and desktop as well as mobile integration.</p>
