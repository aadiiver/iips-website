            <h3 class="text-center"><b> Devi Ahilya University </b></h3>
            <p class="text-justify"> 
                DAVV Indore (formerly, university of Indore) was established in 1964 by an Act of legislature of Madhya Pradesh. 
                The University is supported by both the State Government and the University Grants Commission, New Delhi.
                 The University has been cited in the Commonwealth Year Book (Volume 3, pp. 1740-42, 1990). 
                 It has been bestowed with the center of Excellence award by the Government of Madhya Pradesh. 
                 The University has thirty two level in frontline areas of Computers, Information Technology, Engineering, Biotechnology, 
                 Life Sciences, Social Sciences, Education, Management, etc. It has more than one hundred and fifty faculty members, 
                 five thousand students on the campus, 113 affiliated colleges and more than one lakh ten thousand students enrolled in all. 
                 The teaching programmes conducted at the Teaching University Departments have a multidisciplinary approach.
            </p>
            <p class="text-justify">
                The faculty is Devi Ahilya well qualified and the infrastructure is constantly upgraded, Computer Science, Electronics, 
                Education, Biotechnology, Life Sciences, Business Management, Business Economics, and International Business are typical of 
                its high profile programmes. The University has earned a place for itself in the country on account of the high quality of 
                its instructional programmes, research, consultancy, extension activities and self-supported programmes. 
                The University has an audio Visual Research Center for making educational television films for UGC CEC Countrywide Classroom 
                Programme and training / educational video material functioning since 1991. The center has produced more than 500 films so far,
                some of which has won awards. An inter University Consortium for department of Atomic Energy Facilities was established in 1991
                in the campus itself. The university has well-equipped department of Physical Education, Sports Center, Play Grounds, 
                and Gymnasium etc. to bring out the sporting talent of the students. The University offers selective hostel facilities 
                for outstation students. It has three girls and four boys hostels catering to the requirement of students studying in UTDs.
                Every effort is made to provide accommodation to girl students. However the boys students are provided hostel facilities based
                on availability.
            </p>
